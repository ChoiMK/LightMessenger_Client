package com.light.vo;

import java.io.Serializable;

public class FdVO implements Serializable{
	private String fdRegnum;
	private String fdGr;
	private String fdLt;
	private String fdMem;
	
	
	public String getFdRegnum() {
		return fdRegnum;
	}

	public void setFdRegnum(String fdRegnum) {
		this.fdRegnum = fdRegnum;
	}


	public String getFdMem() {
		return fdMem;
	}

	public void setFdMem(String fdMem) {
		this.fdMem = fdMem;
	}

	public String getFdGr() {
		return fdGr;
	}

	public void setFdGr(String fdGr) {
		this.fdGr = fdGr;
	}

	public String getFdLt() {
		return fdLt;
	}

	public void setFdLt(String fdLt) {
		this.fdLt = fdLt;
	}

}
